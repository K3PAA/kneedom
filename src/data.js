export const dimensions = { x: 25, y: 17 }

const createOneArray = (arrOne, ...arr) => {
  const newArr = arrOne

  for (const array of arr) {
    for (let i = 0; i < array.length; i++) {
      if (array[i] !== 0) {
        if (!newArr[i]) newArr[i] = array[i]
        else newArr[i] = [newArr[i], array[i]]
      }
    }
  }

  return newArr
}

